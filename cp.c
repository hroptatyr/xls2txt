int find_charset(char *name)
{
	const char names[] = "utf8asc\0iso1iso2";
	int l, p;
	l = strlen(name);
	if(l<3 || l>4) return -1;
	for(p=0; names[p]; p+=4)
		if(memcmp(names+p, name, 4)==0)
			return p>>2;
	return -1;
}

static void expand(u8 *s)
{
	u8 *d = uni2cs;
	u8 m = *s++;
	do {
		u8 c = *s++;
		if(c<m) d += c;
		else if(d>=endof(uni2cs)) break;
		else *d++ = c;
	} while(*s);
}

void set_charset(int n)
{
	cs = 0;
	if(n==0) // utf8
		return;

//	memset(uni2cs, 0, sizeof uni2cs);
	expand(fallbacks);

	switch(n) {
		int u;
	case 1: // ascii
		break;
	case 2: // latin 1
		for(u=0x00A0; u<0x0100; u++)
			uni2cs[u-0xA0] = u;
		break;
	case 3: // latin 2
		expand(latin2);
		break;
	}
	cs = uni2cs;
}

static void print_uni_char(u16 u)
{
	unsigned v = u;
	if(v<0x00A0) {
		if(v<0x20 || v>=0x7F)
			v = v==10 ? ' ' : badchar;
	} else if(cs) {
		v -= 0xA0;
		if(v >= sizeof uni2cs || !(v = cs[v]))
			v = badchar;
	} else {
		v = v>>6 | 0xC0;
		if(u >= 0x800) {
			putchar(u>>12 | 0xE0);
			v = v&077 | 0x80;
		}
		putchar(v);
		v = u&077 | 0x80;
	}
	putchar(v);
}

u8 *print_uni(u8 *p, int l, u8 f)
{
	if(f&1)
		while(--l >= 0) {
			print_uni_char(g16(p));
			p += 2;
		}
	else
		while(--l >= 0)
			print_uni_char(*p++);
	return p;
}

